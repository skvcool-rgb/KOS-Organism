"""
KOS Myelination Engine -- Compiles evolved ASTs into permanent Python engines.

When the Dream Mode Tree Swarm evolves a perfect AST for a task,
this module transpiles it into a standalone Python class and saves it
to kos/learned_engines/. The next Awake Mode benchmark auto-imports it.

This is the System-2 -> System-1 bridge:
    - System 2 (Dream Mode): hours of slow evolutionary search
    - Myelination: compile the evolved program into optimized Python
    - System 1 (Awake Mode): execute the compiled engine in milliseconds
"""

import os
